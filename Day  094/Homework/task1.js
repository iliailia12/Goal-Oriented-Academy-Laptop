// Logical Operators (ლოგიკური ოპერატორები)
// დავალება: შექმენით ფუნქცია, რომელიც იღებს სამი რიცხვს და ამოწმებს, ეს სამი რიცხვი ქმნიან სამკუთხედს თუ არა. სამკუთხედის
//  შესანარჩუნებლად წრეწირს სამი გვერდი უნდა აკმაყოფილებდეს: a + b > c, b + c > a და c + a > b.


function isTriangle(a, b, c) {
    return (a + b > c) && (b + c > a) && (c + a > b);
}


console.log(isTriangle(3, 4, 5));
console.log(isTriangle(1, 1, 2)); 